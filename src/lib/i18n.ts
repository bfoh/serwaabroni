export function t(key: string, lang: Language): string {
  return translations[lang]?.[key] || translations.en[key] || key
}

export function useLanguage() {
  const getStoredLang = (): Language => {
    try {
      const stored = localStorage.getItem('serwaabroni_language')
      return stored === 'tw' ? 'tw' : 'en'
    } catch {
      return 'en'
    }
  }

  const setLanguage = (lang: Language) => {
    try {
      localStorage.setItem('serwaabroni_language', lang)
    } catch {
      // ignore
    }
  }

  return { getStoredLang, setLanguage, t: (key: string, lang: Language) => t(key, lang) }
}
